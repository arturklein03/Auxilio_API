        <div class="paragrafo">
            <p>O projeto tem por objetivo analisar os dados disponibilizados pelo governo federal sobre o Auxílio Brasil e observar quais foram as datas dos saques e os valores recebidos pelas pessoas, podendo assim verificar quanto dinheiro foi investido em cada município e as diferenças entre municípios.</p>
